    private void btnRemoveDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveDeptActionPerformed
        if (this.curDepNode.getChildCount() > 0) {
            String msg = "Remove all employee before deleting a department.";
            JOptionPane.showMessageDialog(this, msg);
        } else {
            int response = JOptionPane.showConfirmDialog(this, "Deleting this department - OK");
            if (response == JOptionPane.OK_OPTION) {
                root.remove(this.curDepNode);
                treeDetails.updateUI();
            }
        }
    }//GEN-LAST:event_btnRemoveDeptActionPerformed

    private void btnSaveDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveDeptActionPerformed
        String code = this.txtDeptCode.getText().trim().toUpperCase();
        txtDeptCode.setText(code);
        String name = this.txtDeptName.getText().trim();
        txtDeptName.setText(name);
        if (!validDepDetails()) {
            return;
        }
        if (addNewDep == true) {
            Department_Bai9 newDep = new Department_Bai9(code, name);
            root.add(new DefaultMutableTreeNode(newDep));
        } else {
            ((Department_Bai9) curDepNode.getUserObject()).setDepCode(name);
        }
        this.treeDetails.updateUI();
        this.addNewDep = false;
    }//GEN-LAST:event_btnSaveDeptActionPerformed

